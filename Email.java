public class Email {

	private int id ;
	private String email ;
	private String referencia ;

	public int getId () {

		return id ;

	}

	public void setId ( int i ) {

		id = i ;
	
	}

	public String getEmail () {

		return email ;

	}

	public void setEmail ( String e ) {

		email = e ;
	
	}

	public String getReferencia () {

		return referencia ;

	}

	public void setReferencia ( String r ) {

		referencia = r ;
	
	}

}
